package com.mycompany.model;

public class Item {
	private String name;
	private int healthBoost;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHealthBoost() {
		return healthBoost;
	}
	public void setHealthBoost(int healthBoost) {
		this.healthBoost = healthBoost;
	}

}
